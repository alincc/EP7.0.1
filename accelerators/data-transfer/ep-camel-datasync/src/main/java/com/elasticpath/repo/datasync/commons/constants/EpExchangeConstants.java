package com.elasticpath.repo.datasync.commons.constants;

/**
 * Constants which are set on exchange maps such as properties and headers.
 */
public class EpExchangeConstants {
	/**
	 * The constant used by properties and headers to get the changeset guid from an exchange.
	 */
	public static final String CHANGESET_GUID = "CHANGESET_GUID";

	/**
	 * The constant used by properties to get the changeset status update failure status.
	 */
	public static final String CHANGESET_STATUS_UPDATE_FAILURE = "CHANGESET_STATUS_UPDATE_FAILURE";

	/**
	 * The constant used by properties to get the DST publish success status.
	 */
	public static final String DST_PUBLISH_SUCCESS = "DST_PUBLISH_SUCCESS";

	/**
	 * The constant used by properties to get the changeset result message.
	 */
	public static final String CHANGESET_RESULT_MESSAGE = "CHANGESET_RESULT_MESSAGE";

	/**
	 * The constant used by properties to determine if an email is sent.
	 */
	public static final String DST_SEND_EMAIL_STATUS = "DST_SEND_EMAIL_STATUS";

}
